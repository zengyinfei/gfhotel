              <pre>
    薄刀峰位于湖北罗田县，原名鹤皋峰。旧时常有白鹤栖息于此，古人取诗经中“鹤鸣九皋，声闻于天”之意命名。后因主峰之下卧龙岗形同蛟龙、脊似薄刀，又有冯玉祥部尖刀团曾在此驻扎，改名薄刀峰。薄刀峰自然景观以松奇、石怪、峰险而著称，是一个集旅游、会务、休闲、科研于一体的风景区。
薄刀峰是大别山国家森林公园、大别山国家地质公园的核心景区。海拔1404.2米，景区面积30平方公里，森林覆盖率高达98%，全年平均气温为16.4摄氏度，堪称人间天然“氧吧”。景区内山峰奇秀、植被蓊郁、气候宜人，是游客感受原生风情，体验大自然古朴气息，享受神秘野趣的天赐胜地。</pre>
