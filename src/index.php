            <div class="panel-body"><pre>
    罗田天堂寨风景区境内千米以上的高峰25座，主峰天堂寨海拔1729.13米，为大别山主峰之一，号称"中原第一峰"。因而景区内最神奇的景观便是山，天堂美景数主峰天堂寨为最。
登上主峰，环视天下，则可见十万大山拥拜于下。北望中原，南眺荆楚。凌晨观日出似置身于九天之外，傍晚看日落如在仙宫信步。每当雨过天晴，早晨登主峰观云海更是气象万千，无比壮观。

天堂寨自古以来便是兵家必争之地，历史上在这里发生了不少重要的战役，1947年刘邓大军挺进大别山，天堂寨就是重要的活动区域。</pre>
